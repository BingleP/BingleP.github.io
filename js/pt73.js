  let pt73ActiveSeason = 1;

  function pt73Season(s) {
    pt73ActiveSeason = s;
    document.querySelectorAll('.pt73-tab').forEach((t, i) => {
      t.classList.toggle('active', i + 1 === s);
    });
    const list = document.getElementById('pt73-eplist');
    list.innerHTML = '';
    pt73Data[s].forEach(([id, title], i) => {
      const ep = document.createElement('div');
      ep.className = 'pt73-ep';
      ep.textContent = `E${String(i + 1).padStart(2, '0')} — ${title}`;
      ep.dataset.vid = id;
      ep.onclick = function() { pt73Play(id, `S${s}E${String(i+1).padStart(2,'0')} — ${title}`, this); };
      list.appendChild(ep);
    });
  }

  function pt73Play(videoId, label, el) {
    document.getElementById('pt73-iframe').src = `https://www.youtube.com/embed/${videoId}?autoplay=1&rel=0`;
    document.getElementById('pt73-nowplaying').innerHTML = `▶ <span>${label}</span>`;
    document.querySelectorAll('.pt73-ep').forEach(e => e.classList.remove('playing'));
    el.classList.add('playing');
  }

  pt73Season(1);
